<div class="artwork_header" style="width: 100%; max-width:860px; margin:auto;"><h1 id="buying">How to understand the difference between decorative art and fine art and decorative paintings</h1>
<span class="hr"></span></div>

<div style="width: 100%; max-width:860px; margin:auto;">

<p style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px;">Fine art is defined in <span style="font-style:oblique;">The Harper Collins Dictionary of Art Terms and Techniques</span> (Mayer, 1991, 2nd ed.) as
"art created primarily as an aesthetic expression, to be contemplated or enjoyed for its own sake.
Examples of fine art include painting, drawing, sculpture, print making, and architecture. Decorative art,
defined in the same reference, is "art serving to ornament or embellish an object that has an ulterior
purpose". Simply put, if the "art" is applied to dishes, knick-knacks, or furniture it can be defined as
decorative art. One thing is for certain, fine art is not a print or a poster, no matter the quality.</p>



<p style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px;">In the realm of paintings, the distinction between fine art and decorative art can be somewhat
subjective. The focus of this article is nineteenth-century paintings as fine art. It does not consider, with
some exceptions, paintings done after circa 1890, when postimpressionism began to dominate the art
world. The fin-de- siècle abstract and non-representational art with its focus on raw color, lines and
shapes overshadowed, but did not replace, the clearly defined representations of people, places, and
object of nineteenth-century art. Nor does this article consider art before circa 1820, which marks the
approximate date when artistic style was evolving from the "neoclassicism" of the eighteenth-century to
the "romanticism" and later the "realism" of nineteenth-century art.</p>


<p style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px;">Not every old painting that hangs on the wall is fine art. It may be art in some sense; however, there are
some things to consider before assigning the descriptor "fine" to a painting. Was it painted by a known
artist? Do this artist's paintings hang in museums? Do retail art galleries offer them for sale? Is the
artist listed or mentioned in the numerous art references available? Does it belong to a recognized
"school"? In the art world "school" means national origin (e.g., Flemish), mode (e.g. realism),
movement (impressionism), or locality (e.g. Scalp Level). Most important is that price alone does not
define whether a painting is fine art. While it is true that most fine art paintings and certain artists bring
high prices, there are lesser known artists, but not of lesser talent, whose works also grace the halls of
art galleries. They may be of lesser current value (which is relative anyway); however, who knows what
the future holds. New "old" artists are being rediscovered along with reinvigorated values.</p>



<p style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px;">Don't be a slave to what the experts say, fine art is fine regardless of some opinions—some just may be
"finer" than others. Tastes change, but the skill with which a painting was executed cannot be changed
with the passage of time. Even a novice can discern a poorly balanced composition or one where the
random objects seem to be out of place, rendering the painting "noisy", and perhaps even pointless.</p>


<p style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px;">The following two examples illustrate how people perceive can fine art paintings:</p>

<ol style="list-style-type:decimal; margin-left:20px;">
<li style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px;">A wealthy person buys a painting for $30,000 because it would look "exquisite" above the sofa.
Although the painting is "fine art" in the true sense, this person is purchasing the painting
primarily for its decorative value. The art dealer may have explained that this is "Gilberfluter"
(fictitious name) and that he was an important artist of the Wilberfinster School (fictitious) of
painting. Although they may be interested in such facts, it is of secondary importance. He or
she merely wants the piece to match or complement the home in the decorative sense and they
have the ample discretionary funds to purchase it on the spot. They may or may not consider it
as an investment. After a couple of years when they redecorate, the painting is replaced with

something else. The painting still has value as fine art, but has been "discarded" to make room
for the next decorative piece.</li>
<li style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px;">A couple of more modest means finds a painting they like, by an artist that they are familiar with
and admire, at a cost of $5,000. This painting is also fine art but not carrying a $30,000 price
tag. This second buyer has less "discretionary" funds available for this type of purchase. They
have a mortgage, car payments, and college tuition, but they can't believe that they just
stumbled upon a Martin Blather (fictitious name). Not only is it a Martin Blather, which they've
always wished they could own, but it would be perfect in the den. They'll take down the print of
the Mona Lisa and hang this fortuitous find in its place. The gallery has a lay-away plan and by
making payments they can own it within a year. This painting represents something beyond
mere decoration for this buyer. It has attained real status—it is by a known, collectible artist
that is important to the buyer. After many years the painting still remains the focal point of the
den.</li>
</ol>




<p style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px;">The difference between the two buyers is total instant gratification (first example) versus anticipation of
ownership (second example). In the mind of the second buyer he is truly purchasing fine art. He may
not consider it an investment; he is buying it primarily for the gratification in owning a painting by this
particular artist. The two paintings may be of equal quality by two equally recognized artists, but the
price difference aside, they are not thought of in the same way.</p>


<p style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px;">You who are considering taking a walk on the "art side", don't shy away from nineteenth-century art,
either for investment or pleasure. As renowned nineteenth-century French academic artist William
Adolphe Bouguereau famously said about "modern art&quot;:</p>

<p style="font-size:1.1em; line-height:1.8em; margin:0px; padding-bottom:20px; font-style:oblique;">One shouldn't believe in all those so-called innovations. There is only one nature and
only one way to see it. Nowadays, they want to succeed too fast, this how they go about
inventing new aesthetics pointillism, pipsme! All this is just to make noise.</p>



<p style="font-size:1.1em; line-height:1.8em; margin:0px;">Thankfully, nineteenth-century art is an escape from such "noise".</p>




