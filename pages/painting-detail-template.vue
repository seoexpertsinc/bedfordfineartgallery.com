              <p>Landscape painter Ernest Fredericks, whose given name was Frederick Ernest Swedlun was born in McPherson, Kansas.  It has been said that Frederick was influenced by Swedish artist and fellow Kansan Birger Sandzen, who was teaching at Bethany College in Lindsborg, KS, roughly 30 miles north of McPherson.  Fredericks studied at the Chicago Academy of Fine Art and it was during period he took the name Ernest Fredericks due to prejudice against Swedes that existed at the time.  Fredericks, familiar with the Ozark Mountains, described them as an “artist’s gold mine of color and composition.”  He moved to Eureka Springs, AK in the Ozarks in 1950 where he painted the seasonal splendor of his favorite mountains.  He also resumed using his given name Ernest Swedlun.  He and his son, Glenn Swedlun, an artist in his own right, taught art classes in the Ozarks up until Ernest’s death in 1959.  Fredericks was a member of the Chicago Society of Artists.  He exhibited at the Art Institute of Chicago.  Swedlun’s paintings are in the collections of the Ogden Museum of Southern Art (New Orleans, LA), the State of Illinois permanent art collection, and the Eureka Springs Historical Museum.








